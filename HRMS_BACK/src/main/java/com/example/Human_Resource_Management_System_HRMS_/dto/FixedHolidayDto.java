package com.example.Human_Resource_Management_System_HRMS_.dto;

public class FixedHolidayDto {

	private int month;

	private int day;

	private String name;

	public int getMonth() {
		return month;
	}

	public void setMonth(int month) {
		this.month = month;
	}

	public int getDay() {
		return day;
	}

	public void setDay(int day) {
		this.day = day;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
